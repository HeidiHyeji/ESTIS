package Beans;

public class IMFFFSBean {
	private int imfffsid;
	private String fsid;
	private String party;
	private int missionid;
	
	public IMFFFSBean() {

	}

	public int getImfffsid() {
		return imfffsid;
	}

	public void setImfffsid(int imfffsid) {
		this.imfffsid = imfffsid;
	}

	public String getFsid() {
		return fsid;
	}

	public void setFsid(String fsid) {
		this.fsid = fsid;
	}

	public String getParty() {
		return party;
	}

	public void setParty(String party) {
		this.party = party;
	}

	public int getMissionid() {
		return missionid;
	}

	public void setMissionid(int missionid) {
		this.missionid = missionid;
	}

}
